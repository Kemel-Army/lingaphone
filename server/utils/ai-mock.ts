/**
 * Check if OpenAI API key is available.
 */
export function isAiAvailable(): boolean {
  const apiKey = process.env.OPENAI_API_KEY || useRuntimeConfig().openaiApiKey
  return !!apiKey && apiKey !== 'sk-...' && apiKey.length > 10
}

